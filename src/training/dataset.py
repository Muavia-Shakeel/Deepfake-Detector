"""
src/training/dataset.py
Custom PyTorch Dataset for loading deepfake images.
"""
import torch
from torch.utils.data import Dataset
import pandas as pd
from pathlib import Path

from src.preprocessing.preprocessor import load_image

class DeepfakeDataset(Dataset):
    """
    PyTorch Dataset for deepfake face images.
    Reads the index CSV generated by `dataset_index.py`.
    """
    def __init__(self, index_csv, data_root, split, transform=None):
        """
        Args:
            index_csv (str): Path to the master index CSV.
            data_root (str): Root directory of the processed data.
            split (str): 'train', 'val', or 'test'.
            transform (callable, optional): Albumentations transform pipeline.
        """
        self.data_root = Path(data_root)
        self.transform = transform
        
        df = pd.read_csv(index_csv)
        self.df = df[df['split'] == split].reset_index(drop=True)
        
        self.class_to_idx = {'real': 0, 'fake': 1}

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):
        row = self.df.iloc[idx]
        img_path = self.data_root / row['path']
        label_str = row['label']
        
        # Load image as numpy array
        image = load_image(str(img_path))
        if image is None:
            # Handle case where image loading fails
            # Return a dummy tensor and a special label (-1) to be skipped in training
            print(f"Warning: Failed to load image {img_path}. Skipping.")
            return torch.zeros((3, 224, 224)), -1

        # Apply augmentations
        if self.transform:
            augmented = self.transform(image=image)
            image = augmented['image']
            
        label = self.class_to_idx[label_str]
        
        return image, torch.tensor(label, dtype=torch.long)

def collate_fn(batch):
    """
    Custom collate_fn to filter out samples that failed to load.
    """
    batch = list(filter(lambda x: x is not None and x[1] != -1, batch))
    return torch.utils.data.dataloader.default_collate(batch)
